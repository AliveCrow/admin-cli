/**
 * 下划线转驼峰
 * @param {*} str
 * @returns
 */
export function transformStr (str) {
  str = str.replace(/\//g, '_')
  return str.replace(/(\-|\_)(\w)/g, function (all, $1, $2) {
    return $2.toUpperCase()
  })
}
